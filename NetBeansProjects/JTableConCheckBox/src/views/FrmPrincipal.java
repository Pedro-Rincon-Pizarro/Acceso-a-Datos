    public void inicializarTabla(){
        modeloP.setColumnIdentifiers(columnas);
        JTablePersonas.setModel(modeloP);
        JTablePersonas.setDefaultRenderer(Object.class,new TablaRender());
       
    }
    public void CargarDatosTabla(){
          //Creo una lista con 3 personas
        listaPersonas.add(new Persona("20202044X","Ana","Ruiz",true));
        listaPersonas.add(new Persona("19191919X","Rosa","Gomez",false));
        listaPersonas.add(new Persona("21212121X","Alejandra","Perez",true));
        
       //Paso de la lista al JTable
        for (Persona per : listaPersonas) {  
            Object lineaPer[]={per.getDni(),per.getNombre(),per.getApellidos(),per.getTrabaja()};
            modeloP.addRow(lineaPer);
        }
    }
      
        
       
