    private void ValidarDatos(String nombre, String apellidos, String dni, boolean nmb, boolean ape, boolean dn) throws HeadlessException 
    {
        if (nombre.isEmpty() || apellidos.isEmpty() || dni.isEmpty()) 
        {

            lblCamposOblig.setText("*Todos los campos son obligatorios");

        }
        else if (!nombre.matches("[a-zA-ZáéíóúÁÉÍÓÚñÑ ]{1,40}")) 
        {
            lblErrorNombre.setText("*El Nombre debe tener un máximo de 40 caracteres y no contener números.");
            nmb = true;

        } // Validar apellidos
        if (!apellidos.matches("[a-zA-ZáéíóúÁÉÍÓÚñÑ ]{1,70}")) 
        {
            lblErrorApellidos.setText("*Los Apellidos deben tener un máximo de 70 caracteres y no contener números.");
            ape = true;

        } // Validar DNI/NIE (Formato DNI: 8 números y 1 letra, NIE puede comenzar con X, Y, Z seguido de 7 números y 1 letra)
        if (!dni.matches("([0-9]{8}[A-Za-z])|([XYZ][0-9]{7}[A-Z])")) 
        {
            lblErrorDni.setText("*DNI o NIE inválido. Debe ser de 9 caracteres.");
            dn = true;
        } 
        else 
        {
            NifValidator nif = new NifValidator();
            if (!nif.isValidNifNie(dni)) 
            {
                lblErrorDni.setText("El DNI o NIE introducido no es valido.");
                dn = true;
            }

        }
        if (nmb == false && ape == false && dn == false) 
        {
            EscribirFichero(nombre, apellidos, dni);
        } else 
        {
            JOptionPane.showMessageDialog(null, "No se pueden guardar datos erroneos.");
        }
    }

    private void EscribirFichero(String nombre, String apellidos, String dni) throws HeadlessException 
    {
        JOptionPane.showMessageDialog(null, "Todos los datos son válidos.");
        Persona p1 = new Persona(nombre, apellidos, dni);
        String nomFormat = String.format("%-40s", p1.getNombre());
        String apeFormat = String.format("%-40s", p1.getApellido());
        String dniFormat = p1.getDni();

        try 
        {
            File fichero = new File("salida.txt");
            FileWriter fw = new FileWriter(fichero);
            BufferedWriter bw = new BufferedWriter(fw);

            if (!fichero.exists() && !fichero.isDirectory()) 
            {
                fichero.createNewFile();
            }

            bw.write("Persona: " + p1.getNombre() + p1.getApellido() + p1.getDni() + "\n");

            bw.close();
            fw.close();
            txtNombre.setText("");
            txtApellidos.setText("");
            txtDni.setText("");

        } catch (IOException ex) 
        {
            JOptionPane.showMessageDialog(null, "no se ha podido escribir el fichero.");
        }
    }

