    private void btBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBuscarActionPerformed
        String busqueda = txtBuscar.getText();
        
        for(Actor act : listaActores)
        {
            if(busqueda.equalsIgnoreCase(act.getNombre()))
            {
                tpnPrincipal.setSelectedComponent(pnReparto);
                listActores.setSelectedIndex(act.getCodigo());
                paneDesc.setText(act.getBiografia());
                txtPaneSinopsis.setText("La pasión de Arthur Fleck, un hombre ignorado por "
                + "la sociedad, es hacer reír a la gente. Sin embargo, una "
                + "serie de trágicos sucesos harán que su visión del mundo "
                + "se distorsione considerablemente convirtiéndolo en un brillante criminal.");
                lblBaja.setText("Nos encontramos en reparto: " + busqueda);
                break;
            }
        }
        
    }//GEN-LAST:event_btBuscarActionPerformed

    private void listActoresValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_listActoresValueChanged
        String actorSelec = listActores.getSelectedValue();
        
        
        lblBaja.setText("Nos encontramos en reparto: " + actorSelec);
        
        for(Actor act : listaActores)
        {
            if(act.getNombre().equalsIgnoreCase(actorSelec))
            {
                paneDesc.setText(act.getBiografia());
            }
        }
        if(actorSelec.equalsIgnoreCase("JOAQUIN PHOENIX"))
        {
            paneDesc.setText("Descripcion joaquin");
        }
        else if(actorSelec.equalsIgnoreCase("ROBERT DE NIRO"))
        {
            paneDesc.setText("Descripcion ROBERT");
        }
        else if(actorSelec.equalsIgnoreCase("ZAZIE BEETZ"))
        {
            paneDesc.setText("Descripcion ZAZIE");
        }
        
    }//GEN-LAST:event_listActoresValueChanged

