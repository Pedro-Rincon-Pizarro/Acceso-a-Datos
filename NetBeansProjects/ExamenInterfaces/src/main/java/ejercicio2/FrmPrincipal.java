    private void btReporteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btReporteActionPerformed
        String Reportes="";
        JTextArea area=new JTextArea();
            for (int i = 0; i < tabla.getRowCount(); i++) {
                boolean sel=(boolean)tabla.getValueAt(i, 3);
                if(sel){
                    Reportes+="Nombre : "+tabla.getValueAt(i, 1)+" ; Apellidos : "+tabla.getValueAt(i, 2)+"\n";
                }
            }
            if ("".equals(Reportes)){
                 JOptionPane.showMessageDialog(null, "Antes de obtener los datos, debe de seleccionar por lo menos a un checkbox",
                    "Información",JOptionPane.WARNING_MESSAGE);
            } 
            else {
            area.setText(Reportes);
            JOptionPane.showMessageDialog(this,area);
            }
    }//GEN-LAST:event_btReporteActionPerformed

