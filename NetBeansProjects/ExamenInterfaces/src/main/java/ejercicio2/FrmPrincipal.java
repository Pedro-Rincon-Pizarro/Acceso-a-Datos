    private void tablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseClicked
        columna=tabla.getColumnModel().getColumnIndexAtX(evt.getX());
       row=evt.getY()/tabla.getRowHeight();
       if(columna<=tabla.getColumnCount() && columna>=0 && row<=tabla.getRowCount()&& row>=0){
           Object objeto=tabla.getValueAt(row, columna);
           if(objeto instanceof JCheckBox){
           ((JCheckBox)objeto).doClick();
               JCheckBox botones=(JCheckBox)objeto;
               Object dni=tabla.getValueAt(row, 0);
               if(botones.getName().equals("btn1")){
                    JOptionPane.showMessageDialog(null,"Usuario , usted presionó el boton Modificar del DNI:"+dni.toString());
                
               }else{
                   if(botones.getName().equals("btn2")){
                     JOptionPane.showMessageDialog(null,"Usuario, usted presionó el boton Eliminar del DNI:"+dni.toString());
                   
                   }
               }
           }
       }
    }//GEN-LAST:event_tablaMouseClicked

