    private void btAltaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAltaActionPerformed
        boolean vegetariano = false;
        if(txtNombre.getText().trim().isEmpty())
        {
            txtNombre.setBorder(BorderFactory.createLineBorder(Color.red));
            txtNombre.requestFocus();
            JOptionPane.showMessageDialog(this, "El nombre es obligatorio.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(txtApellidos.getText().trim().isEmpty())
        {
            txtApellidos.setBorder(BorderFactory.createLineBorder(Color.red));
            txtApellidos.requestFocus();
            JOptionPane.showMessageDialog(this, "El apellido es obligatorio.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(txtDeporte.getText().trim().isEmpty())
        {
            txtDeporte.setBorder(BorderFactory.createLineBorder(Color.red));
            txtDeporte.requestFocus();
            JOptionPane.showMessageDialog(this, "El deporte es obligatorio.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(txtEdad.getText().trim().isEmpty() || !txtEdad.getText().matches("\\d+"))
        {
            txtEdad.setBorder(BorderFactory.createLineBorder(Color.red));
            txtEdad.requestFocus();
            JOptionPane.showMessageDialog(this, "La edad del deportista debe ser un n√∫mero entero.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(checkVegetariano.isSelected())
        {
            vegetariano = true;
        }
        
        Deportista deportista = new Deportista(txtNombre.getText(), txtApellidos.getText(),
                txtDeporte.getText(), Integer.parseInt(txtEdad.getText()), vegetariano);
        arrayDeportistas.add(deportista);
        
        Object[] lineaPer = {deportista.getNombre(), deportista.getApellidos(), deportista.getDeporte(), deportista.getEdad(), deportista.isVegetariano()};
        modeloTabla.addRow(lineaPer);
        
        txtNombre.setText("");
        txtApellidos.setText("");
        txtDeporte.setText("");
        txtEdad.setText("");
        checkVegetariano.setSelected(false);
    }//GEN-LAST:event_btAltaActionPerformed

    private void btMediaEdadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btMediaEdadActionPerformed
       
        int edadMedia = 0;
        
        for(Deportista d : arrayDeportistas)
        {
            edadMedia += d.getEdad();
        }
        
        edadMedia = edadMedia /arrayDeportistas.size();
        JOptionPane.showMessageDialog(this, "la media de edad es " + edadMedia, "Error", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btMediaEdadActionPerformed

