    private void btNumVegetarianosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btNumVegetarianosActionPerformed
        int vegetarianos = 0;
        for(Deportista d : arrayDeportistas)
        {
            if(d.isVegetariano())
            {
                vegetarianos++;
            }
        }
        
         JOptionPane.showMessageDialog(this, "el numero de vegetarianos es " + vegetarianos, "Error", JOptionPane.INFORMATION_MESSAGE);
        
    }//GEN-LAST:event_btNumVegetarianosActionPerformed

