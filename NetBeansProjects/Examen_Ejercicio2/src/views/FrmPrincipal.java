    private void btnMostrarPrecioMedioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarPrecioMedioActionPerformed
        // TODO add your handling code here:
        int contador = 0;
        double total = 0;
        for(Producto pro : listaProductos){
            contador++;
            total += pro.getPrecio();
        }
        
        double media = total/contador;
        JOptionPane.showMessageDialog(this, "El precio medio de los productos es:\n" + media);
    }//GEN-LAST:event_btnMostrarPrecioMedioActionPerformed

    private void cbSeleccionarTodoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbSeleccionarTodoActionPerformed
        // TODO add your handling code here:
        if(cbSeleccionarTodo.isSelected()){
            modeloP = new DefaultTableModel(){
                @Override
                public boolean isCellEditable(int row,int col){
                     return columnasEditables[col];
                }
                @Override
                public Class getColumnClass(int index){
                     return tipo[index];
                 }
            }; 
            inicializarTabla();
            for(Producto pro : listaProductos){
                Object lineaPer[]={String.valueOf(pro.getCodProducto()),pro.getProducto(),String.valueOf(pro.getPrecio()), true};
                modeloP.addRow(lineaPer);
            }
            cbSeleccionarTodo.setText("Deseleccionar Todo");
        }
        else{
            modeloP = new DefaultTableModel(){
                @Override
                public boolean isCellEditable(int row,int col){
                     return columnasEditables[col];
                }
                @Override
                public Class getColumnClass(int index){
                     return tipo[index];
                 }
            }; 
            inicializarTabla();
            for(Producto pro : listaProductos){
                Object lineaPer[]={String.valueOf(pro.getCodProducto()),pro.getProducto(),String.valueOf(pro.getPrecio()), false};
                modeloP.addRow(lineaPer);
            }
            cbSeleccionarTodo.setText("Seleccionar Todo");
        }
    }//GEN-LAST:event_cbSeleccionarTodoActionPerformed

