    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
       txtCodigo.setBorder(null);
       txtProducto.setBorder(null);
       txtPrecio.setBorder(null);
        
        boolean valido = false;
        if(validarNumerico.validarEntero(txtCodigo.getText())  && validarTexto.esValido(txtProducto.getText()) && validarNumerico.validarDouble(txtPrecio.getText())) valido = true;
        if(validarNumerico.validarEntero(txtCodigo.getText()) == false) txtCodigo.setBorder(BorderFactory.createLineBorder(Color.RED, 1));
        if(validarTexto.esValido(txtProducto.getText()) == false) txtProducto.setBorder(BorderFactory.createLineBorder(Color.RED, 1));
        if(validarNumerico.validarDouble(txtPrecio.getText()) == false) txtPrecio.setBorder(BorderFactory.createLineBorder(Color.RED, 1));
        
        if(valido){
            listaProductos.add(new Producto(Integer.parseInt(txtCodigo.getText()),txtProducto.getText(),Double.parseDouble(txtPrecio.getText()),false));

            Object lineaPer[]={txtCodigo.getText(),txtProducto.getText(),txtPrecio.getText(),false};
            modeloP.addRow(lineaPer);
           
            txtCodigo.setText("");
            txtProducto.setText("");
            txtPrecio.setText("");
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnMostrarSeleccionadosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarSeleccionadosActionPerformed
        // TODO add your handling code here:
        String seleccionados = "";
        for(Producto pro : listaProductos){
            if(pro.isAccion()) seleccionados += pro.toString() + "\n";
        }
        JOptionPane.showMessageDialog(this, "Los productos seleccionados son:\n" + seleccionados);
    }//GEN-LAST:event_btnMostrarSeleccionadosActionPerformed

